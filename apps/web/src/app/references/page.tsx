function DatasetCharts() {
  const [monthly, setMonthly] = useState<MonthlyData | null>(null);
  const [backlog, setBacklog] = useState<BacklogData | null>(null);
  const [forecast, setForecast] = useState<ForecastData | null>(null);
  const [optz, setOptz] = useState<OptimizationData | null>(null);
  const [year, setYear] = useState<string>("all");

  useEffect(() => {
    fetch("/data/rain_complaints_monthly.json").then((r) => r.json()).then(setMonthly).catch(() => {});
    fetch("/data/ward_backlog.json").then((r) => r.json()).then(setBacklog).catch(() => {});
    fetch("/data/forecast.json").then((r) => r.json()).then(setForecast).catch(() => {});
    fetch("/data/optimization.json").then((r) => r.json()).then(setOptz).catch(() => {});
  }, []);

  // List of years derived from the monthly data — for the dropdown.
  const years = useMemo(() => {
    if (!monthly) return [];
    const set = new Set(monthly.months.map((m) => m.slice(0, 4)));
    return Array.from(set).sort();
  }, [monthly]);

  // Filter the monthly series by selected year (or "all" = all 60 months).
  const filteredMonthly = useMemo(() => {
    if (!monthly) return null;
    if (year === "all") return monthly;
    const idx: number[] = [];
    monthly.months.forEach((m, i) => { if (m.startsWith(year)) idx.push(i); });
    return {
      ...monthly,
      months: idx.map((i) => monthly.months[i]),
      road_complaints: idx.map((i) => monthly.road_complaints[i]),
      rain_mm: idx.map((i) => monthly.rain_mm[i]),
    };
  }, [monthly, year]);

  // Top 12 wards by total complaints for the bar chart
  const topWards = useMemo(() => {
    if (!backlog) return [];
    return [...backlog.wards]
      .sort((a, b) => b.total - a.total)
      .slice(0, 12);
  }, [backlog]);

  // Department split aggregated across all wards (uses the BBMP category mapping)
  const deptSplit = useMemo(() => {
    if (!backlog) return [];
    const dept: Record<string, number> = {};
    for (const w of backlog.wards) {
      for (const [cat, c] of Object.entries(w.by_category || {})) {
        dept[cat] = (dept[cat] ?? 0) + c.total;
      }
    }
    return Object.entries(dept)
      .map(([k, v]) => ({ name: k, value: v }))
      .sort((a, b) => b.value - a.value);
  }, [backlog]);

  return (
    <section
      className="rounded-3xl border p-6 sm:p-8"
      style={{
        background: "linear-gradient(180deg, rgba(191,79,54,0.04) 0%, rgb(var(--bg-surface)) 100%)",
        borderColor: "rgba(191, 79, 54, 0.30)",
      }}
    >
      <div className="mb-6 flex flex-wrap items-end justify-between gap-3">
        <div>
          <div className="flex items-center gap-1.5 text-xs font-semibold uppercase tracking-wider"
            style={{ color: "rgb(var(--accent))" }}>
            <BarChart3 className="h-3.5 w-3.5" /> Live charts · real Bengaluru data
          </div>
          <h2 className="mt-1 text-2xl font-semibold sm:text-3xl">What the numbers actually say</h2>
          <p className="mt-2 max-w-2xl text-base text-ink-600">
            Every chart below is rendered from the same files the production agents read.
            No mock data. Pick a year to filter the time-series.
          </p>
        </div>

        {years.length > 0 && (
          <label className="flex items-center gap-2 rounded-lg px-3 py-2 text-sm"
            style={{
              background: "rgb(var(--bg-surface))",
              border: "1px solid rgb(var(--border-color))",
              color: "rgb(var(--text-primary))",
            }}>
            <span className="text-xs font-semibold uppercase tracking-wider"
              style={{ color: "rgb(var(--text-muted))" }}>Year</span>
            <select
              value={year}
              onChange={(e) => setYear(e.target.value)}
              className="bg-transparent font-mono font-semibold outline-none"
              style={{ color: "rgb(var(--accent))" }}
            >
              <option value="all">All years</option>
              {years.map((y) => (<option key={y} value={y}>{y}</option>))}
            </select>
          </label>
        )}
      </div>

      <div className="grid gap-5 lg:grid-cols-2">
        {/* Chart 1 — Rainfall vs road complaints */}
        <ChartCard
          icon={CloudRain}
          title="Bengaluru monthly rainfall vs road complaints"
          subtitle={year === "all" ? "5 years · 60 months · all 243 BBMP wards" : `${year} · 12 months`}
          source="rain_complaints_monthly.json"
        >
          {filteredMonthly ? (
            <DualAxisChart
              labels={filteredMonthly.months}
              bars={filteredMonthly.rain_mm}
              line={filteredMonthly.road_complaints}
              barLabel="Rainfall (mm)"
              lineLabel="Road complaints"
            />
          ) : <ChartSkeleton />}
        </ChartCard>

        {/* Chart 2 — Forecast vs actual */}
        <ChartCard
          icon={TrendingUp}
          title="2025 forecast — model vs naive baseline"
          subtitle={forecast
            ? `R²=${forecast.train_r2} train · ${forecast.test.skill_vs_naive_pct.toFixed(1)}% better than naive`
            : ""}
          source="forecast.json"
        >
          {forecast ? (
            <MultiLineChart
              labels={forecast.test.series.months}
              series={[
                { name: "actual",   data: forecast.test.series.actual,   color: "#0f172a" },
                { name: "forecast", data: forecast.test.series.forecast, color: "rgb(var(--accent))" },
                { name: "naive",    data: forecast.test.series.naive,    color: "#94a3b8" },
              ]}
            />
          ) : <ChartSkeleton />}
        </ChartCard>

        {/* Chart 3 — Top 12 wards by backlog */}
        <ChartCard
          icon={MapPinned}
          title="Top 12 wards by complaint backlog"
          subtitle={backlog
            ? `${backlog.meta.n_wards} wards · ${backlog.meta.total_complaints.toLocaleString()} total complaints · ${backlog.meta.total_open.toLocaleString()} still open`
            : ""}
          source="ward_backlog.json"
        >
          {topWards.length ? (
            <BarListChart
              rows={topWards.map((w) => ({
                label: w.ward,
                value: w.total,
                open: w.open,
                resolution: w.resolution,
              }))}
            />
          ) : <ChartSkeleton />}
        </ChartCard>

        {/* Chart 4 — Dept workload split + MILP optimizer summary */}
        <ChartCard
          icon={Target}
          title="MILP optimizer · backlog cleared per department"
          subtitle={optz
            ? `${optz.crews_deployed ?? optz.budget} crews · ${optz.backlog_cleared.toLocaleString()} cleared (${optz.pct_open_cleared}% of open)`
            : ""}
          source="optimization.json"
        >
          {optz ? (
            <DeptBarChart
              data={Object.entries(optz.cleared_by_department)
                .filter(([, v]) => v > 0)
                .map(([k, v]) => ({
                  label: k,
                  cleared: v,
                  crews: optz.crews_by_department[k] ?? 0,
                }))
                .sort((a, b) => b.cleared - a.cleared)}
            />
          ) : <ChartSkeleton />}
        </ChartCard>
      </div>

      {/* Department distribution — full-width strip */}
      {deptSplit.length > 0 && (
        <div className="mt-5">
          <ChartCard
            icon={BarChart3}
            title="Complaint mix across BBMP categories (all 243 wards)"
            subtitle="Where the 127k complaint backlog actually lives"
            source="ward_backlog.json"
          >
            <StackedRowChart data={deptSplit} />
          </ChartCard>
        </div>
      )}
    </section>
  );
}

