/** Dual-axis: rainfall (bars) + complaints (line). */
function DualAxisChart({ labels, bars, line, barLabel, lineLabel }:
  { labels: string[]; bars: number[]; line: number[]; barLabel: string; lineLabel: string }) {
  const W = 600, H = 180, P = { l: 32, r: 32, t: 12, b: 26 };
  const maxBar = Math.max(1, ...bars);
  const maxLine = Math.max(1, ...line);
  const x = (i: number) => P.l + (i / Math.max(1, labels.length - 1)) * (W - P.l - P.r);
  const yBar = (v: number) => H - P.b - (v / maxBar) * (H - P.t - P.b);
  const yLine = (v: number) => H - P.b - (v / maxLine) * (H - P.t - P.b);
  const barW = Math.max(2, (W - P.l - P.r) / labels.length - 2);
  const path = line.map((v, i) => `${i === 0 ? "M" : "L"} ${x(i)} ${yLine(v)}`).join(" ");

  return (
    <div className="w-full">
      <svg viewBox={`0 0 ${W} ${H}`} className="w-full" style={{ overflow: "visible" }}>
        {/* y grid */}
        {[0.25, 0.5, 0.75, 1].map((t) => (
          <line key={t} x1={P.l} x2={W - P.r} y1={P.t + (1 - t) * (H - P.t - P.b)} y2={P.t + (1 - t) * (H - P.t - P.b)}
            stroke="rgb(var(--border-light))" strokeDasharray="2 3" />
        ))}
        {/* bars — rainfall */}
        {bars.map((v, i) => (
          <rect key={i} x={x(i) - barW / 2} y={yBar(v)} width={barW} height={H - P.b - yBar(v)}
            fill="rgba(59, 130, 246, 0.55)" rx={1} />
        ))}
        {/* line — complaints */}
        <path d={path} fill="none" stroke="rgb(var(--accent))" strokeWidth={2.2} />
        {line.map((v, i) => (
          <circle key={i} cx={x(i)} cy={yLine(v)} r={2.2} fill="rgb(var(--accent))" />
        ))}
        {/* x labels — sparse */}
        {labels.map((l, i) => {
          const show = labels.length <= 14 || i % Math.ceil(labels.length / 12) === 0;
          if (!show) return null;
          return <text key={i} x={x(i)} y={H - 8} textAnchor="middle" fontSize="9" fill="rgb(var(--text-muted))">{l.slice(2)}</text>;
        })}
      </svg>
      <div className="mt-2 flex items-center justify-center gap-4 text-xs" style={{ color: "rgb(var(--text-secondary))" }}>
        <span className="flex items-center gap-1.5">
          <span className="inline-block h-2 w-3" style={{ background: "rgba(59, 130, 246, 0.55)" }} /> {barLabel}
        </span>
        <span className="flex items-center gap-1.5">
          <span className="inline-block h-0.5 w-3" style={{ background: "rgb(var(--accent))" }} /> {lineLabel}
        </span>
      </div>
    </div>
  );
}

/** Multi-line — actual / forecast / naive. */
function MultiLineChart({ labels, series }:
  { labels: string[]; series: { name: string; data: number[]; color: string }[] }) {
  const W = 600, H = 180, P = { l: 32, r: 32, t: 12, b: 26 };
  const max = Math.max(1, ...series.flatMap((s) => s.data));
  const x = (i: number) => P.l + (i / Math.max(1, labels.length - 1)) * (W - P.l - P.r);
  const y = (v: number) => H - P.b - (v / max) * (H - P.t - P.b);

  return (
    <div className="w-full">
      <svg viewBox={`0 0 ${W} ${H}`} className="w-full" style={{ overflow: "visible" }}>
        {[0.25, 0.5, 0.75, 1].map((t) => (
          <line key={t} x1={P.l} x2={W - P.r}
            y1={P.t + (1 - t) * (H - P.t - P.b)} y2={P.t + (1 - t) * (H - P.t - P.b)}
            stroke="rgb(var(--border-light))" strokeDasharray="2 3" />
        ))}
        {series.map((s) => {
          const path = s.data.map((v, i) => `${i === 0 ? "M" : "L"} ${x(i)} ${y(v)}`).join(" ");
          return (
            <g key={s.name}>
              <path d={path} fill="none" stroke={s.color} strokeWidth={s.name === "forecast" ? 2.5 : 1.8}
                strokeDasharray={s.name === "naive" ? "4 3" : undefined} />
              {s.data.map((v, i) => (
                <circle key={i} cx={x(i)} cy={y(v)} r={s.name === "forecast" ? 2.5 : 1.8} fill={s.color} />
              ))}
            </g>
          );
        })}
        {labels.map((l, i) => (
          <text key={i} x={x(i)} y={H - 8} textAnchor="middle" fontSize="9" fill="rgb(var(--text-muted))">{l.slice(5)}</text>
        ))}
      </svg>
      <div className="mt-2 flex flex-wrap items-center justify-center gap-4 text-xs"
        style={{ color: "rgb(var(--text-secondary))" }}>
        {series.map((s) => (
          <span key={s.name} className="flex items-center gap-1.5">
            <span className="inline-block h-0.5 w-3" style={{ background: s.color }} /> {s.name}
          </span>
        ))}
      </div>
    </div>
  );
}

/** Horizontal bar list — used for top wards. */
function BarListChart({ rows }: { rows: { label: string; value: number; open: number; resolution: number }[] }) {
  const max = Math.max(1, ...rows.map((r) => r.value));
  return (
    <div className="space-y-1.5">
      {rows.map((r) => {
        const pct = (r.value / max) * 100;
        const openPct = (r.open / r.value) * 100;
        return (
          <div key={r.label}>
            <div className="flex items-center justify-between text-xs">
              <span className="font-medium" style={{ color: "rgb(var(--text-primary))" }}>{r.label}</span>
              <span className="font-mono" style={{ color: "rgb(var(--text-muted))" }}>
                {r.value.toLocaleString()} · {(r.resolution * 100).toFixed(0)}% resolved
              </span>
            </div>
            <div className="relative mt-0.5 h-3 overflow-hidden rounded-md"
              style={{ background: "rgb(var(--bg-surface-hover))" }}>
              <div className="absolute inset-y-0 left-0 rounded-md"
                style={{ width: `${pct}%`, background: "rgba(16, 185, 129, 0.65)" }} />
              <div className="absolute inset-y-0 left-0 rounded-l-md"
                style={{ width: `${(pct * openPct) / 100}%`, background: "rgba(220, 38, 38, 0.65)" }} />
            </div>
          </div>
        );
      })}
      <div className="mt-3 flex items-center justify-center gap-4 text-xs"
        style={{ color: "rgb(var(--text-secondary))" }}>
        <span className="flex items-center gap-1.5">
          <span className="inline-block h-2 w-3 rounded-sm" style={{ background: "rgba(220, 38, 38, 0.65)" }} /> Still open
        </span>
        <span className="flex items-center gap-1.5">
          <span className="inline-block h-2 w-3 rounded-sm" style={{ background: "rgba(16, 185, 129, 0.65)" }} /> Resolved
        </span>
      </div>
    </div>
  );
}

/** Department backlog cleared — vertical bar + crew count. */
function DeptBarChart({ data }: { data: { label: string; cleared: number; crews: number }[] }) {
  const max = Math.max(1, ...data.map((d) => d.cleared));
  return (
    <div className="space-y-2">
      {data.map((d) => {
        const pct = (d.cleared / max) * 100;
        return (
          <div key={d.label}>
            <div className="flex items-center justify-between text-xs">
              <span className="font-semibold">{d.label}</span>
              <span className="font-mono" style={{ color: "rgb(var(--text-muted))" }}>
                {d.cleared.toLocaleString()} cleared · {d.crews} crews
              </span>
            </div>
            <div className="mt-0.5 h-3 overflow-hidden rounded-md"
              style={{ background: "rgb(var(--bg-surface-hover))" }}>
              <div className="h-full rounded-md"
                style={{ width: `${pct}%`, background: "rgb(var(--accent))" }} />
            </div>
          </div>
        );
      })}
    </div>
  );
}

/** Stacked horizontal — share of categories across the whole city. */
function StackedRowChart({ data }: { data: { name: string; value: number }[] }) {
  const total = data.reduce((s, d) => s + d.value, 0) || 1;
  const palette = [
    "rgb(var(--accent))", "#2563eb", "#10b981", "#f59e0b",
    "#a855f7", "#ec4899", "#0ea5e9", "#84cc16",
  ];
  return (
    <div>
      <div className="flex h-7 w-full overflow-hidden rounded-lg">
        {data.map((d, i) => {
          const pct = (d.value / total) * 100;
          if (pct < 0.5) return null;
          return (
            <div
              key={d.name}
              title={`${d.name} · ${d.value.toLocaleString()} (${pct.toFixed(1)}%)`}
              style={{ width: `${pct}%`, background: palette[i % palette.length] }}
            />
          );
        })}
      </div>
      <div className="mt-3 grid grid-cols-2 gap-x-4 gap-y-1 text-xs sm:grid-cols-3 lg:grid-cols-4"
        style={{ color: "rgb(var(--text-secondary))" }}>
        {data.map((d, i) => {
          const pct = (d.value / total) * 100;
          return (
            <div key={d.name} className="flex items-center gap-1.5 truncate">
              <span className="h-2 w-3 shrink-0 rounded-sm" style={{ background: palette[i % palette.length] }} />
              <span className="truncate">{d.name}</span>
              <span className="ml-auto font-mono" style={{ color: "rgb(var(--text-muted))" }}>
                {pct.toFixed(1)}%
              </span>
            </div>
          );
        })}
      </div>
    </div>
  );
}
